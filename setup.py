def detect_rtlsdr_devices() -> list:
    """Detect RTL-SDR USB devices via lsusb."""
    import subprocess
    devices = []
    try:
        result = subprocess.run(
            ["lsusb"], capture_output=True, text=True, timeout=5,
        )
        for line in result.stdout.splitlines():
            # RTL2832U vendor:product IDs
            if "0bda:2838" in line or "0bda:2832" in line:
                devices.append(line.strip())
    except (FileNotFoundError, subprocess.TimeoutExpired):
        pass
    return devices


def ask_sdr_device(args) -> dict:
    """Detect and select RTL-SDR device."""
    if args.non_interactive:
        return {
            "device_index": args.sdr_device if args.sdr_device is not None else 0,
            "gain": args.sdr_gain if args.sdr_gain is not None else 40,
        }

    print()
    print(f"  {BOLD}Step 6: RTL-SDR Device{NC}")
    print(f"  {'─' * 50}")
    print()

    devices = detect_rtlsdr_devices()

    if not devices:
        warn("No RTL-SDR devices detected via USB.")
        info("Make sure your dongle is plugged in before starting the containers.")
        info("Using default: device index 0, gain 40")
        return {"device_index": 0, "gain": 40}

    if len(devices) == 1:
        info(f"Found 1 RTL-SDR: {devices[0]}")
        device_index = 0
    else:
        info(f"Found {len(devices)} RTL-SDR devices:")
        for i, dev in enumerate(devices):
            print(f"    [{i}] {dev}")
        print()
        idx = prompt(f"Which device to use for FM scanning? (0-{len(devices)-1})", default="0")
        try:
            device_index = int(idx)
            if device_index < 0 or device_index >= len(devices):
                device_index = 0
                warn(f"Invalid index, using 0")
        except ValueError:
            device_index = 0

    gain = prompt("RTL-SDR gain (0-49, or 'auto')", default="40")

    return {
        "device_index": device_index,
        "gain": gain,
    }


