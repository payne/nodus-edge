def interactive_frequency_picker(
    repeaters: list,
    current_core: list,
    current_candidates: list,
    metro_coverage: list,
) -> tuple:
    """Let the operator pick frequencies interactively.

    Returns (core_hz, candidate_hz).
    """
    # Build a map of metro coverage for display
    coverage_count = {}
    for fd in metro_coverage:
        coverage_count[fd["frequency_hz"]] = fd["node_count"]

    print()
    print(f"  {BOLD}Available repeaters (sorted by distance):{NC}")
    print()
    print(f"  {'#':>3s}  {'Freq MHz':>10s}  {'Callsign':8s}  {'Dist':>5s}  {'Other':>5s}  {'Status'}")
    print(f"  {'─' * 55}")

    # Build selectable list
    selectable = [{"frequency_hz": NATIONAL_SIMPLEX_2M_HZ, "label": "National simplex", "distance": 0}]

    for rptr in repeaters:
        try:
            freq_hz = int(float(rptr["Frequency"]) * 1_000_000)
            selectable.append({
                "frequency_hz": freq_hz,
                "label": rptr.get("Callsign", ""),
                "distance": rptr.get("_distance_miles", "?"),
                "city": rptr.get("Nearest City", ""),
            })
        except (ValueError, KeyError):
            continue

    for i, item in enumerate(selectable[:30], 1):
        fhz = item["frequency_hz"]
        freq_mhz = fhz / 1_000_000
        label = item.get("label", "")
        dist = item.get("distance", "")
        others = coverage_count.get(fhz, 0)
        in_core = "*" if fhz in current_core else " "
        others_str = f"{others}" if others > 0 else "-"
        print(f"  {i:>3d}  {freq_mhz:>10.3f}  {label:8s}  {str(dist):>5s}  {others_str:>5s}  {in_core}")

    if len(selectable) > 30:
        print(f"  ... {len(selectable) - 30} more available")

    print()
    print(f"  * = currently selected as core")
    print(f"  'Other' column shows how many other metro nodes cover that frequency")
    print()

    selection = prompt(
        "Enter frequency numbers to use as core (comma-separated, e.g. 1,3,5,7)",
        default=",".join(str(i) for i in range(1, min(11, len(selectable) + 1))),
    )

    try:
        indices = [int(x.strip()) - 1 for x in selection.split(",") if x.strip()]
        new_core = []
        for idx in indices:
            if 0 <= idx < len(selectable):
                fhz = selectable[idx]["frequency_hz"]
                if fhz not in new_core:
                    new_core.append(fhz)
        if new_core:
            # Remaining become candidates
            core_set = set(new_core)
            new_candidates = [
                item["frequency_hz"]
                for item in selectable
                if item["frequency_hz"] not in core_set
            ]
            info(f"Selected {len(new_core)} core frequencies, {len(new_candidates)} candidates.")
            return new_core, new_candidates
    except ValueError:
        warn("Invalid selection, keeping original frequencies.")

    return current_core, current_candidates


