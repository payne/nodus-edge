RF_PRESETS = {
    "1": ("Urban / Noisy", "Near repeater sites, lots of RF", 40, 10.0),
    "2": ("Suburban / Moderate", "Default, good balance", 30, 6.0),
    "3": ("Rural / Quiet", "Weak signals, low noise floor", 20, 4.0),
}


def scan_noise_floor(device_index: int = 0, gain: str = "40") -> dict | None:
    """Scan 2m band with rtl_power to measure noise floor.

    Runs a quick sweep of 144-148 MHz and computes the 25th-percentile
    power level as the noise floor estimate.  Maps the result to
    recommended squelch / SNR settings.

    Returns dict with noise_floor_db, squelch_threshold,
    airband_squelch_snr_db, and label — or None on failure.
    """
    import shutil
    import subprocess
    import tempfile

    if not shutil.which("rtl_power"):
        return None

    fd, csv_path = tempfile.mkstemp(suffix=".csv")
    os.close(fd)

    try:
        cmd = [
            "rtl_power",
            "-d", str(device_index),
            "-f", "144M:148M:25k",  # 2m band, 25 kHz bins
            "-i", "2",               # 2s integration per window
            "-1",                     # single sweep then exit
        ]
        if gain and str(gain).lower() != "auto":
            cmd.extend(["-g", str(gain)])
        cmd.append(csv_path)

        subprocess.run(cmd, capture_output=True, text=True, timeout=30)

        # Parse CSV: date, time, Hz_low, Hz_high, Hz_step, samples, dB, dB, ...
        powers = []
        with open(csv_path) as f:
            for line in f:
                parts = line.strip().split(",")
                if len(parts) > 6:
                    for val in parts[6:]:
                        try:
                            powers.append(float(val.strip()))
                        except ValueError:
                            pass

        if not powers:
            return None

        return classify_noise_floor(powers)
    except (subprocess.TimeoutExpired, OSError):
        return None
    finally:
        try:
            os.unlink(csv_path)
        except OSError:
            pass


def classify_noise_floor(powers: list) -> dict:
    """Map a list of power readings (dB) to squelch recommendations.

    Uses the 25th percentile as the noise floor estimate to avoid
    active signals skewing the result upward.
    """
    powers_sorted = sorted(powers)
    idx_25 = max(0, len(powers_sorted) // 4)
    noise_floor = powers_sorted[idx_25]

    if noise_floor > -45:
        sq, snr, label = 45, 12.0, "Very noisy"
    elif noise_floor > -55:
        sq, snr, label = 40, 10.0, "Urban / Noisy"
    elif noise_floor > -65:
        sq, snr, label = 30, 6.0, "Suburban / Moderate"
    else:
        sq, snr, label = 20, 4.0, "Rural / Quiet"

    return {
        "noise_floor_db": round(noise_floor, 1),
        "squelch_threshold": sq,
        "airband_squelch_snr_db": snr,
        "label": label,
    }


def ask_rf_environment(args, sdr_config: dict | None = None) -> dict:
    """Ask about RF environment to set squelch thresholds."""
    # CLI override takes priority
    if args.squelch is not None:
        return {
            "squelch_threshold": args.squelch,
            "airband_squelch_snr_db": 6.0,
        }

    if args.non_interactive:
        if getattr(args, "auto_squelch", False):
            device_index = (sdr_config or {}).get("device_index", 0)
            gain = (sdr_config or {}).get("gain", 40)
            result = scan_noise_floor(device_index, str(gain))
            if result:
                return {
                    "squelch_threshold": result["squelch_threshold"],
                    "airband_squelch_snr_db": result["airband_squelch_snr_db"],
                }
        return {"squelch_threshold": 30, "airband_squelch_snr_db": 6.0}

    print()
    print(f"  {BOLD}Step 7: RF Environment{NC}")
    print(f"  {'─' * 50}")
    print()
    print("  Squelch controls how much signal is needed to start recording.")
    print("  Too low = static-only recordings. Too high = missed weak signals.")
    print()

    for key, (name, desc, sq, snr) in RF_PRESETS.items():
        print(f"    [{key}] {BOLD}{name}{NC}")
        print(f"        {desc} (squelch {sq}, SNR {snr} dB)")

    # Only show auto-detect if rtl_power is available
    import shutil
    has_rtl_power = shutil.which("rtl_power") is not None
    if has_rtl_power:
        print(f"    [A] {BOLD}Auto-detect{NC}")
        print(f"        Scan 2m band with RTL-SDR to measure noise floor (~10s)")
    print()

    default = "A" if has_rtl_power else "2"
    choice = prompt("Select RF environment", default=default)

    if choice.upper() == "A" and has_rtl_power:
        device_index = (sdr_config or {}).get("device_index", 0)
        gain = (sdr_config or {}).get("gain", 40)
        info("Scanning 2m band for noise floor — this takes about 10 seconds...")
        result = scan_noise_floor(device_index, str(gain))
        if result:
            info(f"Noise floor: {result['noise_floor_db']:.1f} dB → {result['label']}")
            info(f"Recommended squelch: {result['squelch_threshold']}, SNR: {result['airband_squelch_snr_db']} dB")
            print()
            if prompt_yn("Accept these settings?"):
                return {
                    "squelch_threshold": result["squelch_threshold"],
                    "airband_squelch_snr_db": result["airband_squelch_snr_db"],
                }
            warn("Falling back to manual selection.")
            choice = prompt("Select preset (1/2/3)", default="2")
        else:
            warn("Scan failed — RTL-SDR may be unavailable or busy.")
            warn("Falling back to manual selection.")
            choice = prompt("Select preset (1/2/3)", default="2")

    if choice not in RF_PRESETS:
        warn(f"Unknown choice '{choice}', using Suburban / Moderate.")
        choice = "2"

    _, _, squelch, snr = RF_PRESETS[choice]
    info(f"Squelch threshold: {squelch}, SNR: {snr} dB")
    print(f"  Tip: raise squelch if you get static-only recordings, lower if missing weak signals.")

    return {"squelch_threshold": squelch, "airband_squelch_snr_db": snr}


