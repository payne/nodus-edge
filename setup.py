def prompt(question: str, default: str = "", validator=None) -> str:
    """Prompt user for input with optional default and validation."""
    suffix = f" [{default}]" if default else ""
    while True:
        try:
            answer = input(f"  {question}{suffix}: ").strip()
        except EOFError:
            answer = ""
        if not answer and default:
            answer = default
        if validator:
            err = validator(answer)
            if err:
                error(err)
                continue
        return answer


def prompt_yn(question: str, default_yes: bool = True) -> bool:
    """Prompt for yes/no answer."""
    hint = "Y/n" if default_yes else "y/N"
    while True:
        try:
            answer = input(f"  {question} [{hint}]: ").strip().lower()
        except EOFError:
            answer = ""
        if not answer:
            return default_yes
        if answer in ("y", "yes"):
            return True
        if answer in ("n", "no"):
            return False
        error("Please enter y or n.")


def load_zip_metro() -> dict:
    """Load the bundled zip->metro CBSA mapping if available."""
    if not ZIP_METRO_PATH.exists():
        return {}
    try:
        with open(ZIP_METRO_PATH) as f:
            data = json.load(f)
        return data.get("zips", {})
    except (json.JSONDecodeError, KeyError):
        return {}


def lookup_metro(zip_code: str, city: str, zip_metro: dict) -> str:
    """Resolve metro slug from zip code using CBSA mapping, with city fallback."""
    # Try CBSA mapping first (handles Elkhorn->omaha, Scottsdale->phoenix)
    if zip_code in zip_metro:
        return zip_metro[zip_code]
    # Fallback: slugify the city name from the zip API
    return slugify_city(city)


def slugify_city(city: str) -> str:
    """Normalize city name to a slug (fallback when CBSA mapping misses)."""
    slug = city.lower()
    slug = re.sub(r"[^a-z0-9]", "", slug)
    return slug[:20]


def haversine_distance(lat1: float, lon1: float, lat2: float, lon2: float) -> float:
    """Distance between two points in miles."""
    R = 3959  # Earth radius in miles
    lat1_r, lat2_r = math.radians(lat1), math.radians(lat2)
    dlat = math.radians(lat2 - lat1)
    dlon = math.radians(lon2 - lon1)
    a = math.sin(dlat / 2) ** 2 + math.cos(lat1_r) * math.cos(lat2_r) * math.sin(dlon / 2) ** 2
    return R * 2 * math.atan2(math.sqrt(a), math.sqrt(1 - a))


