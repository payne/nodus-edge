def is_raspberry_pi() -> bool:
    """Detect if running on a Raspberry Pi (too slow for local Whisper)."""
    import platform
    arch = platform.machine()
    if arch not in ("aarch64", "armv7l"):
        return False
    # Check /proc/cpuinfo for Raspberry Pi
    try:
        with open("/proc/cpuinfo") as f:
            cpuinfo = f.read()
        if "Raspberry Pi" in cpuinfo or "BCM2" in cpuinfo:
            return True
    except OSError:
        pass
    # Check device-tree model
    try:
        with open("/proc/device-tree/model") as f:
            model = f.read()
        if "Raspberry Pi" in model:
            return True
    except OSError:
        pass
    # Fallback: aarch64 with < 4GB RAM is likely a Pi or similar SBC
    try:
        with open("/proc/meminfo") as f:
            for line in f:
                if line.startswith("MemTotal:"):
                    mem_kb = int(line.split()[1])
                    if mem_kb < 4_000_000:
                        return True
                    break
    except (OSError, ValueError):
        pass
    return False


def ask_whisper(args) -> str:
    """Ask for Whisper transcription endpoint."""
    pi_detected = is_raspberry_pi()

    if args.non_interactive:
        if pi_detected and args.whisper_url is None:
            return "https://nodusrf.com/v1"
        return args.whisper_url if args.whisper_url is not None else "http://whisper:8000"

    print()
    print(f"  {BOLD}Step 6: Transcription{NC}")
    print(f"  {'─' * 50}")
    print()

    if pi_detected:
        url = args.whisper_url or "https://nodusrf.com/v1"
        info("Raspberry Pi detected. Using NodusNet GPU pool for transcription.")
        info(f"Whisper endpoint: {url}")
    else:
        default_url = args.whisper_url if args.whisper_url is not None else "http://whisper:8000"

        url = prompt(
            "Whisper transcription endpoint",
            default=default_url,
        )

    if url:
        # Skip connectivity test for Docker internal DNS (container isn't running yet)
        if "://whisper:" in url:
            info(f"Using Docker internal DNS: {url}")
        else:
            # Test connectivity
            try:
                req = Request(url)
                req.add_header("User-Agent", USER_AGENT)
                urlopen(req, timeout=5)
                info(f"Whisper reachable at {url}")
            except Exception:
                warn(f"Could not reach {url} — you can fix this in .env later.")
    else:
        info("Transcription disabled. Set RECEPT_WHISPER_API_URL in .env to enable.")

    return url


