def load_offline_repeaters() -> list:
    """Load the bundled offline 2m repeater database."""
    path = REPEATERS_2M_PATH
    if not path.exists():
        return []
    try:
        with open(path) as f:
            data = json.load(f)
        return data.get("repeaters", [])
    except (json.JSONDecodeError, OSError):
        return []


def fetch_repeaters(lat: float, lon: float, state_name: str,
                    radius_miles: float = DEFAULT_RADIUS_MILES) -> tuple:
    """Find 2m repeaters near location from bundled offline data.

    Returns (repeaters_list, metadata_dict).
    """
    metadata = {
        "center_lat": lat,
        "center_lon": lon,
        "radius_miles": radius_miles,
        "fetched_at": datetime.now(timezone.utc).isoformat(),
        "source": "offline-bundle",
        "band": "2m",
    }

    print()
    info(f"Searching bundled repeater data for 2m repeaters within {radius_miles} miles...")

    all_repeaters = load_offline_repeaters()
    if not all_repeaters:
        warn("Offline repeater database not found.")
        warn(f"Looked in: {REPEATERS_2M_PATH}")
        metadata["total_repeaters"] = 0
        return [], metadata

    info(f"Loaded {len(all_repeaters)} US 2m repeaters from offline database.")

    # Filter by distance
    nearby = []
    for rptr in all_repeaters:
        try:
            rlat = float(rptr.get("Lat", 0))
            rlon = float(rptr.get("Long", 0))
            if rlat == 0 or rlon == 0:
                continue
            dist = haversine_distance(lat, lon, rlat, rlon)
            if dist <= radius_miles:
                rptr["_distance_miles"] = round(dist, 1)
                nearby.append(rptr)
        except (ValueError, TypeError):
            continue

    # Deduplicate by (Frequency, Callsign), sort by distance
    seen = set()
    unique = []
    for rptr in sorted(nearby, key=lambda x: x.get("_distance_miles", 9999)):
        key = (rptr.get("Frequency"), rptr.get("Callsign"))
        if key not in seen:
            seen.add(key)
            unique.append(rptr)

    metadata["total_repeaters"] = len(unique)
    info(f"Found {len(unique)} repeaters within {radius_miles} miles.")

    return unique, metadata


def split_frequencies(repeaters: list) -> tuple:
    """Split repeaters into core and candidate frequency lists.

    Returns (core_hz, candidate_hz).
    Core: national simplex + top 10 closest active repeaters.
    Candidate: remaining repeaters within radius.
    """
    core_hz = [NATIONAL_SIMPLEX_2M_HZ]

    # Filter to operational repeaters
    active = [r for r in repeaters
              if r.get("Operational Status", "").strip().lower() in ("on-air", "")]

    for rptr in active[:CORE_FREQ_COUNT]:
        try:
            freq_hz = int(float(rptr["Frequency"]) * 1_000_000)
            if freq_hz not in core_hz:
                core_hz.append(freq_hz)
        except (ValueError, KeyError):
            continue

    core_set = set(core_hz)
    candidate_hz = []
    for rptr in active[CORE_FREQ_COUNT:]:
        try:
            freq_hz = int(float(rptr["Frequency"]) * 1_000_000)
            if freq_hz not in core_set:
                candidate_hz.append(freq_hz)
        except (ValueError, KeyError):
            continue

    return core_hz, candidate_hz


def check_bandwidth(frequencies: list, repeaters: list) -> tuple:
    """Check if frequencies fit within RTL-SDR usable bandwidth.

    Returns (fits: bool, span_mhz: float, max_mhz: float, over_freqs: list).
    over_freqs is a list of (freq_hz, callsign, distance) for frequencies
    that would need to be dropped to fit.
    """
    if len(frequencies) < 2:
        return True, 0.0, RTLSDR_USABLE_BW_HZ / 1_000_000, []

    sorted_freqs = sorted(frequencies)
    span = sorted_freqs[-1] - sorted_freqs[0]
    max_bw = RTLSDR_USABLE_BW_HZ
    fits = span <= max_bw

    over_freqs = []
    if not fits:
        # Identify which edge frequencies would need to be dropped.
        # Try trimming from the high end first, then low end.
        center = (sorted_freqs[0] + sorted_freqs[-1]) // 2
        half_bw = max_bw // 2
        for freq_hz in sorted_freqs:
            if freq_hz < center - half_bw or freq_hz > center + half_bw:
                # Find repeater info
                callsign = ""
                distance = ""
                for rptr in repeaters:
                    try:
                        if int(float(rptr["Frequency"]) * 1_000_000) == freq_hz:
                            callsign = rptr.get("Callsign", "")
                            distance = rptr.get("_distance_miles", "?")
                            break
                    except (ValueError, KeyError):
                        continue
                over_freqs.append((freq_hz, callsign, distance))

    return fits, span / 1_000_000, max_bw / 1_000_000, over_freqs


def ask_repeaters(args, lat: float, lon: float, state_name: str) -> tuple:
    """Fetch repeaters and get user confirmation.

    Returns (core_hz, candidate_hz, repeaters, metadata).
    """
    radius = args.radius

    print()
    print(f"  {BOLD}Step 3: Repeater Discovery{NC}")
    print(f"  {'─' * 50}")

    # Band selection
    if not args.non_interactive:
        print()
        print(f"    [1] {BOLD}2 Meter{NC} (144-148 MHz)")
        print(f"    [2] {BOLD}70 Centimeter{NC} (420-450 MHz)")
        print(f"    [3] {BOLD}1.25 Meter{NC} (222-225 MHz)")
        print()
        band_choice = prompt("Which band will you be monitoring?", default="1")
    else:
        band_choice = "1"

    band_map = {"1": "2m", "2": "70cm", "3": "1.25m"}
    selected_band = band_map.get(band_choice, "2m")

    if selected_band != "2m":
        warn(f"Offline repeater data for {selected_band} is not yet available.")
        info("The national simplex frequency will be configured.")
        info("Add repeater frequencies manually in .env after setup.")
        # Return just simplex for the selected band
        simplex_map = {"70cm": 446_000_000, "1.25m": 223_500_000}
        simplex_hz = simplex_map.get(selected_band, NATIONAL_SIMPLEX_2M_HZ)
        metadata = {
            "center_lat": lat, "center_lon": lon,
            "radius_miles": radius,
            "fetched_at": datetime.now(timezone.utc).isoformat(),
            "source": "manual", "band": selected_band,
        }
        return [simplex_hz], [], [], metadata

    repeaters, metadata = fetch_repeaters(lat, lon, state_name, radius)
    core_hz, candidate_hz = split_frequencies(repeaters)

    print()
    info(f"Found {len(repeaters)} repeaters in the 2m band within {radius} miles.")
    print()

    if core_hz:
        print(f"  {BOLD}Core frequencies (always monitored):{NC}")
        for freq_hz in core_hz:
            freq_mhz = freq_hz / 1_000_000
            # Find matching repeater info
            label = ""
            if freq_hz == NATIONAL_SIMPLEX_2M_HZ:
                label = "National simplex"
            else:
                for rptr in repeaters:
                    try:
                        if int(float(rptr["Frequency"]) * 1_000_000) == freq_hz:
                            cs = rptr.get("Callsign", "")
                            dist = rptr.get("_distance_miles", "?")
                            city = rptr.get("Nearest City", "")
                            label = f"{cs}  ({dist} mi) {city}"
                            break
                    except (ValueError, KeyError):
                        continue
            print(f"    {freq_mhz:>10.3f} MHz  {label}")

    if candidate_hz:
        print()
        print(f"  {BOLD}Candidate frequencies (promoted when active): {len(candidate_hz)}{NC}")
        # Show first 5 candidates
        for freq_hz in candidate_hz[:5]:
            freq_mhz = freq_hz / 1_000_000
            for rptr in repeaters:
                try:
                    if int(float(rptr["Frequency"]) * 1_000_000) == freq_hz:
                        cs = rptr.get("Callsign", "")
                        dist = rptr.get("_distance_miles", "?")
                        city = rptr.get("Nearest City", "")
                        print(f"    {freq_mhz:>10.3f} MHz  {cs}  ({dist} mi) {city}")
                        break
                except (ValueError, KeyError):
                    continue
        if len(candidate_hz) > 5:
            print(f"    ... and {len(candidate_hz) - 5} more")

    if not repeaters:
        warn("No 2m repeaters found in your area.")
        info("146.520 MHz (national simplex) will be your only core frequency.")
        info("You can add frequencies manually in the .env file later.")

    # Bandwidth check: do all core frequencies fit in one RTL-SDR?
    fits, span_mhz, max_mhz, over_freqs = check_bandwidth(core_hz, repeaters)
    if not fits:
        print()
        warn(f"SDR bandwidth exceeded: your core frequencies span {span_mhz:.1f} MHz,")
        warn(f"but one RTL-SDR dongle can only cover ~{max_mhz:.1f} MHz at a time.")
        print()
        print(f"  An SDR captures a continuous {BOLD}slice of spectrum{NC} — it's not like a")
        print(f"  radio that hops between frequencies. All your frequencies must fit")
        print(f"  within that slice, or the edge ones won't be received.")
        print()
        if over_freqs:
            print(f"  {BOLD}Frequencies outside the usable window:{NC}")
            for freq_hz, callsign, distance in over_freqs:
                label = f"{callsign}  ({distance} mi)" if callsign else ""
                print(f"    {RED}{freq_hz / 1_000_000:>10.3f} MHz{NC}  {label}")
        print()
        print(f"  Options:")
        print(f"    1. {BOLD}Drop the out-of-range frequencies{NC} to fit within one dongle")
        print(f"    2. {BOLD}Add a second RTL-SDR dongle{NC} to cover additional spectrum")
        print(f"    3. {BOLD}Keep them anyway{NC} (edge frequencies may have degraded reception)")
        print()

        if not args.non_interactive:
            choice = prompt("Choose (1/2/3)", default="3")
            if choice == "1":
                drop_set = {f for f, _, _ in over_freqs}
                core_hz = [f for f in core_hz if f not in drop_set]
                info(f"Trimmed to {len(core_hz)} core frequencies within SDR bandwidth.")
            elif choice == "2":
                info("Multi-dongle: configure the second dongle in .env after setup.")
                info("Set RECEPT_FM_AIRBAND_CENTER_FREQ_HZ to optimize placement.")

    if not args.non_interactive:
        print()
        if not prompt_yn("Accept these frequencies? (you can always change them later)"):
            info("You can edit frequencies in the .env file after setup.")

    return core_hz, candidate_hz, repeaters, metadata


